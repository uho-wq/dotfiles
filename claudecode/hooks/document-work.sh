#!/bin/bash
# Claude Code work documentation hook
# Records completed tasks to .uho directory with timestamped filename

# Get project directory
PROJECT_DIR="${CLAUDE_PROJECT_DIR:-$(pwd)}"
UHO_DIR="${PROJECT_DIR}/.uho"

# Create .uho directory if it doesn't exist
mkdir -p "$UHO_DIR"

# Get timestamp for filename (yyyymmddHHmmss format)
FILE_TIMESTAMP=$(date '+%Y%m%d%H%M%S')
UH_FILE="${UHO_DIR}/${FILE_TIMESTAMP}.md"

# Get readable timestamp for content
TIMESTAMP=$(date '+%Y-%m-%d %H:%M:%S')

# Read JSON input from stdin
INPUT=$(cat)

# Debug: Save raw JSON input for inspection
DEBUG_FILE="${UHO_DIR}/debug_${FILE_TIMESTAMP}.json"
echo "$INPUT" | jq '.' > "$DEBUG_FILE" 2>/dev/null || echo "$INPUT" > "$DEBUG_FILE"

# Parse available information from the Stop event
SUMMARY=$(echo "$INPUT" | jq -r '.summary // .message // "Task completed"' 2>/dev/null || echo "Task completed")

# Extract tool information if available
TOOL_INFO=$(echo "$INPUT" | jq -r '
  if .tools then
    .tools | to_entries | map("- \(.value.name // .key): \(.value.count // 1) times") | join("\n")
  elif .tool_uses then
    "Tools used: \(.tool_uses | length)"
  else
    ""
  end
' 2>/dev/null || echo "")

# Extract conversation context if available
CONTEXT=$(echo "$INPUT" | jq -r '
  if .context then
    .context
  elif .conversation then
    .conversation
  else
    ""
  end
' 2>/dev/null || echo "")

# Create new work log file with enhanced information
cat > "$UH_FILE" << EOF
# Work History Log
# Auto-generated by Claude Code hooks

## [$TIMESTAMP]

### Project
$PROJECT_DIR

### Summary
$SUMMARY

### Details
$TOOL_INFO

$(if [ -n "$CONTEXT" ] && [ "$CONTEXT" != "null" ] && [ "$CONTEXT" != "" ]; then
  echo "### Context"
  echo "$CONTEXT"
fi)

### Debug Information
Raw event data saved to: \`debug_${FILE_TIMESTAMP}.json\`

---

EOF

# Truncate summary for notification
SHORT_SUMMARY=$(echo "$SUMMARY" | head -1 | cut -c1-80)
notify "Work documented: ${SHORT_SUMMARY}" 2>/dev/null || true

exit 0
