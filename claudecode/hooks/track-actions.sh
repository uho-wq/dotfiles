#!/bin/bash
# Claude Code action tracking hook
# Records detailed tool usage in real-time

# Get project directory and timestamp
PROJECT_DIR="${CLAUDE_PROJECT_DIR:-$(pwd)}"
UHO_DIR="${PROJECT_DIR}/.uho"
mkdir -p "$UHO_DIR"

# Get current session date for grouping
SESSION_DATE=$(date '+%Y%m%d')
SESSION_FILE="${UHO_DIR}/session_${SESSION_DATE}.md"

# Read JSON input from stdin
INPUT=$(cat)

# Extract tool information
TOOL_NAME=$(echo "$INPUT" | jq -r '.tool_name // "Unknown"' 2>/dev/null || echo "Unknown")
TIMESTAMP=$(date '+%H:%M:%S')

# Initialize session file if it doesn't exist
if [[ ! -f "$SESSION_FILE" ]]; then
  cat > "$SESSION_FILE" << 'HEADER'
# Work Session Log
# Auto-generated by Claude Code hooks

HEADER
  echo "## Session: $(date '+%Y-%m-%d')" >> "$SESSION_FILE"
  echo "" >> "$SESSION_FILE"
fi

# Record action based on tool type
case "$TOOL_NAME" in
  "Bash")
    COMMAND=$(echo "$INPUT" | jq -r '.tool_input.command // ""' 2>/dev/null)
    DESCRIPTION=$(echo "$INPUT" | jq -r '.tool_input.description // "No description"' 2>/dev/null)
    cat >> "$SESSION_FILE" << EOF

### [$TIMESTAMP] Command Execution
**Description:** $DESCRIPTION
\`\`\`bash
$COMMAND
\`\`\`

EOF
    ;;

  "Edit")
    FILE_PATH=$(echo "$INPUT" | jq -r '.tool_input.file_path // ""' 2>/dev/null)
    OLD_STRING=$(echo "$INPUT" | jq -r '.tool_input.old_string // ""' 2>/dev/null | head -c 200)
    NEW_STRING=$(echo "$INPUT" | jq -r '.tool_input.new_string // ""' 2>/dev/null | head -c 200)
    cat >> "$SESSION_FILE" << EOF

### [$TIMESTAMP] File Edit
**File:** \`$FILE_PATH\`

<details>
<summary>Changes</summary>

**Before:**
\`\`\`
${OLD_STRING}...
\`\`\`

**After:**
\`\`\`
${NEW_STRING}...
\`\`\`
</details>

EOF
    ;;

  "Write")
    FILE_PATH=$(echo "$INPUT" | jq -r '.tool_input.file_path // ""' 2>/dev/null)
    CONTENT_PREVIEW=$(echo "$INPUT" | jq -r '.tool_input.content // ""' 2>/dev/null | head -c 200)
    cat >> "$SESSION_FILE" << EOF

### [$TIMESTAMP] File Creation
**File:** \`$FILE_PATH\`

<details>
<summary>Content preview</summary>

\`\`\`
${CONTENT_PREVIEW}...
\`\`\`
</details>

EOF
    ;;

  "Read")
    FILE_PATH=$(echo "$INPUT" | jq -r '.tool_input.file_path // ""' 2>/dev/null)
    cat >> "$SESSION_FILE" << EOF

### [$TIMESTAMP] File Read
**File:** \`$FILE_PATH\`

EOF
    ;;

  *)
    # Generic tool tracking
    TOOL_INPUT=$(echo "$INPUT" | jq -r '.tool_input // {} | to_entries | map("\(.key): \(.value)") | join(", ")' 2>/dev/null | head -c 200)
    cat >> "$SESSION_FILE" << EOF

### [$TIMESTAMP] $TOOL_NAME
**Parameters:** $TOOL_INPUT

EOF
    ;;
esac

exit 0
